package tools;

import javax.swing.ImageIcon;

/**
 * An interface for tools that will be used for drawing.
 * 
 * @author Shannon Murphy
 * @version 20 February 2015
 */
public interface Tool {
    
    /**
     * Returns the name of the Tool. 
     * @return the name of the Tool.
     */
    String getName();

    /**
     * Returns the ImageIcon for the Tool.
     * @return the ImageIcon for the Tool.
     */
    ImageIcon getIcon();
    
    /**
     * Returns the mnemonic key value for this Tool.
     * @return the mnemonic for this Tool.
     */
    int getMnemonic();
    
    /**
     * Returns a String representation of the tool.
     * @return a String representation of the tool.
     */
    String toString();
}
